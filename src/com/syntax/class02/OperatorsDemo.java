package com.syntax.class02;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number=10;
       // int number=20; //we can't name two boxes with the same name

        System.out.println(number);

        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2);

        /*
        if you have to store whole numbers always use int
        if you have to store decimal numbers always use double
        if you have to store character you can always use String
         */
    }
}
