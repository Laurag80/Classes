package com.syntax.review02;

public class Casting {
    public static void main(String[] args) {
        /*
        converting/ converting one datatype into another
        1. widening
        2. narrowing
         */
        //widening
        double d=5;
        System.out.println(d);

        //narrowing
        int i=(int)5.0;
        System.out.println(i);

        byte b=(byte)128;
        System.out.println(b);



    }
}
