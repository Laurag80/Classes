public class Loops {
    public static void main(String[] args) {

        // while loop
        int a=1;
        while (a<=3){
            System.out.println("I am while loop");
            a++;
        }
        int b=1;
        do{
            System.out.println("I am while");
        }while (b>=3);

        }
    }

