package ReplitPractice;

public class ForLoop056 {
    public static void main(String[] args) {

        // Create a for loop that prints out even numbers from 2 to 14 using if condition

        for (int i = 2; i <= 14; i += 2) {
            if (i % 2 <= 14) {
                System.out.println(i);
            }
        }
    }
}